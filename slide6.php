			The College of Global Studies at Purdue University is committed to supporting you in your efforts to promote education abroad and international education. We value the relationships we have established with study abroad advisors, administrators and faculty at institutions around the globe. We partner with you and support your efforts in many ways including:

			<h3>Campus Visits</h3>
			The College of Global Studies prides itself on our knowledgeable and experienced staff and we are available to visit your campus to speak or present to your faculty, administrators, staff and students. Our staff includes individuals who are experts in nearly all areas of education abroad, including programming, finances and scholarships, curriculum matching, health and safety, assessment and evaluation, internships and experiential education, first year programming, and student services. We invite you to reach out to our staff to support your work on campus.

			The members of our Institutional Relations team are the primary contacts for our U.S. partners and available for campus presentations to your faculty, administrators and staff. They are specialists who are knowledgeable about Purdue's program offerings and have visited most of our program sites. For newer advisors, the team members can serve as a valuable resource for learning about the field. For seasoned advisors, the campus visit can provide a forum to update you on program developments and to learn more about your goals, process and needs.
			In collaboration with our program managers and program representatives, our IR team members are happy to work with you to provide general student support as well as conduct pre-departure orientations or other preparatory efforts for students enrolled or considering enrolling in Purdue programs.
			Our Academic Affairs team members are available to meet with administrators and faculty to discuss curriculum matters, program design, and assessment activities among other academic issues.
			Additionally, we have staff available to visit your institution to talk about the critical and often complex topics of health and safety (including international compliance with Title IX and the Clery Act), financing study abroad and the many issues relating to ensuring student success during their overseas study.
			Affiliation and Direct Billing Agreements
			Many of our partner institutions choose to be billed directly rather than the program participant being billed. We advise students to ensure they work with their home institution to discuss the details about the cost of participation in our programs. If you are interested in a direct billing agreement, please contact your institutional representative.

			<h3> Scholarship Program </h3>
