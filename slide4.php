			We aim to help students for whom financial assistance can mean the difference between participating on an Purdue program and staying home. Purdue scholarships are awarded to eligible applicants based on need.

			In addition to Purdue scholarships, information is provided here on other opportunities to financially support your study abroad experience.


 			SCHOLARSHIPS
			Approximately 70% of study abroad students who apply for aid from Purdue receive some form of award. Typical awards are as follows:

			Semester scholarship awards range between $500-2,500 with an average award of $1,400.
			Summer scholarship awards range between $250-1,000 with an average award of $700.
			You may still receive a scholarship from Purdue even if you already receive aid from your home school.

			The scholarship program is intended for students from other universities studying with Purdue; students already attending Purdue University are not eligible for this scholarship but may wish to review other scholarship opportunities. 


HOW TO APPLY
Complete the Purdue Scholarship Application. Students complete the top portion; a representative of the Financial Aid Office at your university should complete the remainder.

Completed forms should be returned by email attachment to Jennifer Darvas (darvasj@Purdue.edu). Alternatively, forms can be faxed to: 215.572.2174, Attn: Jennifer Darvas


DEADLINES
Applications are received on a rolling basis:

You are welcome to submit a scholarship application up to one month prior to the scheduled departure date for your program
If you have not been accepted to your program when your scholarship application is received, a decision will be deferred until after you have been admitted; otherwise, you should expect within one month.
General Scholarships
In addition to the Arcadia Study Abroad Scholarship, you are encouraged to pursue other scholarship opportunities.

ASHLEY SOULÉ CONROY FOUNDATION SCHOLARSHIP
Each term, the foundation awards up to three $4,000 scholarships.  To be eligible for this scholarship, students must be participating on a program lasting 14 weeks or longer


BENJAMIN A. GILMAN INTERNATIONAL SCHOLARSHIP
Students receiving a Pell Grant may be eligible for this scholarship, which provides up to $5,000 for any semester abroad, including summer. Gilman scholarship recipients who apply for an Purdue Scholarship are guaranteed a scholarship of at least $1,000 from us for semester and year programs and $500 for summer programs.


FUND FOR EDUCATION ABROAD SCHOLARSHIP
The Fund for Education Abroad (FEA) offers scholarships to students studying abroad. The foundation has a strong preference for, but is not limited to, students studying a foreign language, participating in an academically rigorous program, attending in a non-traditional destination, and/or representing an under-represented group for education abroad, such as a minority or a community college student. 


UMN SCHOLARSHIP DATABASE
The University of Minnesota maintains an extensive database of available study abroad funding and scholarship offerings.  This is a great resource to search for funding opportunities.

